        edge_colors = [EDGE_COLOURS.get(e.get("reaction_type",""), "#888")
                       for _,_,e in G.edges(data=True)]

        # -- draw --------------------------------------------------------------
        fig, ax = plt.subplots(figsize=figsize)
        ax.set_facecolor("#F5F7FA")
        ax.set_title(title, fontsize=13, fontweight="bold", pad=12)

        nx.draw_networkx_nodes(G, pos, ax=ax, node_size=650,
                               node_color=node_colors, alpha=0.93,
                               linewidths=0.8, edgecolors="#555")

        node_labels = {n: self.node_label(n).split("]")[-1].strip()[:16]
                       for n in G.nodes}
        nx.draw_networkx_labels(G, pos, labels=node_labels, ax=ax,
                                font_size=6.5, font_color="#111")

        # networkx >=3.2 requires connectionstyle as a plain string;
        # older builds (and some matplotlib versions) may receive a
        # ConnectionStyle object -- guard both cases.
        _cs = "arc3,rad=0.12"
        nx.draw_networkx_edges(G, pos, ax=ax, edge_color=edge_colors,
                               arrows=True, arrowsize=13, alpha=0.65,
                               connectionstyle=_cs, node_size=650)

        # Edge labels where DeltaE is available
        elabels = {}
        for s, d, e in G.edges(data=True):
            val = e.get(edge_label)
            if val is not None and abs(val) < max_label_eV:
                elabels[(s, d)] = f"{val:+.2f}"
        nx.draw_networkx_edge_labels(G, pos, edge_labels=elabels, ax=ax,
                                     font_size=5.5, label_pos=0.35,
                                     bbox=dict(fc="white", ec="none",
                                               boxstyle="round,pad=0.1", alpha=0.7))

        # Row annotations
        for y, lbl in [(2.5, "Monomers"), (0.0, "Dimers"), (-2.5, "References")]:
            ax.text(-max(len(monomers), len(dimers), len(refs)) * 1.2 - 0.5, y,
                    lbl, fontsize=8, color="#555", va="center", style="italic")

        # Legend
        legend = [
            mpatches.Patch(color=NODE_COLOURS["monomer"],   label="Monomer"),
            mpatches.Patch(color=NODE_COLOURS["dimer"],     label="Dimer"),
            mpatches.Patch(color=NODE_COLOURS["reference"], label="Reference (H2O, HCOOH, H2)"),
            mpatches.Patch(color="#2E7D32", label="Substitution (isodesmic)"),
            mpatches.Patch(color="#1565C0", label="Coordination / Decoordination (CN+/-1)"),
            mpatches.Patch(color="#6D4C41", label="Association / Dissociation"),
        ]
        if show_broken and self.broken_structures:
            n_br = len(self.broken_structures)
